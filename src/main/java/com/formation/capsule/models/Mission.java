package com.formation.capsule.models;

public class Mission {
	
	private String name;
	private Number flight;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Number getFlight() {
		return flight;
	}
	public void setFlight(Number flight) {
		this.flight = flight;
	}

}
