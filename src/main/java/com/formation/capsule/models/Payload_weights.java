package com.formation.capsule.models;

public class Payload_weights {
	
	private String id;
	private String name;
	private Number kg;
	private Number lb;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Number getKg() {
		return kg;
	}
	public void setKg(Number kg) {
		this.kg = kg;
	}
	public Number getLb() {
		return lb;
	}
	public void setLb(Number lb) {
		this.lb = lb;
	}
	
	

}
